package a10lib.awindow;

public interface MouseTracker {

	/**
	 * Get current the x position of the mouse cursor
	 * 
	 * @return current the x position of the mouse cursor
	 */
	public int getMouseX();

	/**
	 * Get current y position of the mouse cursor
	 * 
	 * @return current y position of the mouse cursor
	 */
	public int getMouseY();

}
